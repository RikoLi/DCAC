    def forward(self, img, label_cond, labels=None):
        """Take image `img` as input. The image is encoded by the autoencoder
        and then sent into LDM with noise."""
        assert self.max_timestep_sampled <= self.num_timesteps
        ts = min(self.max_timestep_sampled, self.num_timesteps)
        x = self.autoencoder.encode_for_ldm(img)
        t = torch.randint(0, ts, (x.shape[0],), device=self.device).long()
        return self.p_losses(x, t, label_cond=label_cond, labels=labels)

    def q_sample(self, x_start, t, noise=None):
        noise = default(noise, lambda: torch.randn_like(x_start))
        return (extract_into_tensor(self.sqrt_alphas_cumprod, t, x_start.shape) * x_start +
                extract_into_tensor(self.sqrt_one_minus_alphas_cumprod, t, x_start.shape) * noise)

