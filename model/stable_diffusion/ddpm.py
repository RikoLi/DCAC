        l_simple_weight = cfg.SD.DDPM.L_SIMPLE_WEIGHT
        parameterization = cfg.SD.DDPM.PARAMETERIZATION
        use_positional_encodings = cfg.SD.DDPM.USE_POSITIONAL_ENCODINGS
        learn_logvar = cfg.SD.DDPM.LEARN_LOGVAR
